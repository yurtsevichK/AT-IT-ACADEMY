package by.it.yurtsevich.homeTasks.homeTask_day1;

public class Task1_2 {
    /* Используя while вывести все числа от 0 до 25 в одну строку через пробел.
    Пример: */
    public static void main(String[] args) {
        int a = 0;
        do {
            System.out.print(a++ + " ");
        } while (a != 26);
    }
}
